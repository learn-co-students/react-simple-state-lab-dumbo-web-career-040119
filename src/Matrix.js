import React from 'react';
import Cell from './Cell'

export default class Matrix extends React.Component {

  genRow = (vals) => {
    return vals.map((val) => {
      return <Cell value={val}/>
    })
  }

  genMatrix = () => (
    this.props.values.map(rowVals => <div className="row"> { this.genRow(rowVals) } </div>)
  )

  render() {
    return (
      <div id="matrix">
        {this.genMatrix()}
      </div>
    )
  }

}

